// while loop

let index = 0
while (index <= 10) {
    //console.log(`the value of index: ${index}`);
    index = index + 2
}

let myArray = ["sm", "ds", "as"]

let arr = 0
while (arr < myArray.length) {
    //console.log(`value is: ${myArray[arr]}`);
    arr = arr + 1
}

//do-while

let score1 = 1

do {
    console.log(`score is ${score1}`);
    score1++
} while (score1 <= 10);

let score2 = 11

do {
    console.log(`score is ${score2}`);
    score2++
} while (score2 <= 10);